#include <stdio.h>
#include <stdint.h>
#include <inttypes.h>

int main() {
    uint32_t N;          // 32‑битное беззнаковое число
    int K;               // количество бит для сдвига (1 ≤ K ≤ 31)

    // Ввод данных
    scanf("%" SCNu32 "%d", &N, &K);

    // Циклический сдвиг вправо на K бит:
    // часть, которая выходит справа (N >> K), становится старшими битами,
    // а часть, которая должна перейти из младших битов в старшие (N << (32-K)),
    // добавляется через ИЛИ.
    uint32_t result = (N >> K) | (N << (32 - K));

    // Вывод результата
    printf("%" PRIu32 "\n", result);

    return 0;
}
